# 튜플은 리스트와 거의 유사함
# 가장 중요한 차이점은 '요솟값의 변경 불가'

# 프로그램 실행 중 요솟값이 변하지 않기를 바란다면... 튜플을 활용해보자.
# 수시로 값이 변경 돼야 한다면 리스트를 사용하자. 실제 프로그램에서는 리스트를 훨씬 많이 사용함

# t1 = (1, 2, 'a', 'b')

# 튜플 요솟값 삭제, 변경 불가
# del t1[0]
# t1[0] = 'c'
# print(t1)
## 당연히 되지 않는다, 튜플은 요솟값들을 한 번 정하면 지우거나 변경할 수 없다.

# 인덱싱하기
t1 = (1, 2, 3, 'a', 'b')
print(t1[0])

# 슬라이싱 하기
print(t1[4:])
# 튜플은 '1개'의 요소만 가질 때는 요소 뒤에 반드시 쉼표(,)를 붙여야 한다. 즉 출력값은 ('b',)

# 튜플 더하기
t2 = ('c', 'd', 4, 5)
print(t1 + t2)

# 튜플 곱하기
t3 = t2 * 3
print(t3)

